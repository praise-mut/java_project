    private void confirmcodebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmcodebtnActionPerformed
        String email = emailtxt.getText().trim(); // same field
        String enteredOtp = otptxt.getText().trim(); // OTP input field

        if (enteredOtp.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter the OTP", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            service.OtpServiceInterface otpService = main.RmiClient.getOtpService();
            boolean isValid = otpService.validateOtp(email, enteredOtp);

            if (isValid) {
                JOptionPane.showMessageDialog(this, "OTP verified successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                // âž• Continue to registration or password reset
                new Resetpasswd().setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Invalid OTP. Please try again.", "Failure", JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error verifying OTP: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_confirmcodebtnActionPerformed

